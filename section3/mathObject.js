console.log(Math.PI);
console.log(Math.round(4.9));
console.log(Math.abs(-456));
console.log(Math.pow(2, 3));
console.log(Math.floor(3.678));
console.log(Math.ceil(3.678));
console.log(Math.random()); // 0 to 1 normalmente.
console.log(Math.random() * 10); // 0 to 10 normalmente.
