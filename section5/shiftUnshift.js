// mesma coisa que o push e o pop, entretanto ocorre no começo do array
// shift: remove o primeiro item do array
// unshift: adiciona um item no começo do array
let listaArray = ["banana", "maçã", "laranja", "pera", "uva"];
console.log(listaArray);
listaArray.shift();
console.log(listaArray);
listaArray.unshift("banana");
console.log(listaArray);
