// reverse = method to reverse the order of the elements in an array
let letters = ["a", "b", "c", "d", "e"];
console.log(letters.reverse()); // ['e', 'd', 'c', 'b', 'a']
// join = method to join all the elements of an array into a string => retorna uma string, parece um concat, mas não é exatamente igual ao concat
let letters2 = ["a", "b", "c", "d", "e"];
console.log(letters2.join()); // "abcde" - sem o separador -> JOIN RETORNA UMA STRING
let numbers = [1, 2, 3, 4, 5];
console.log(numbers.join()); // "1,2,3,4,5" - com o separador -> JOIN RETORNA UMA STRING
