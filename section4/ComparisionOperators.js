// // Comparison
// > // greater than
// < // less than
// >= // greater than or equal to
// <= // less than or equal to
// == // equal to
// != // not equal to
// === // equal to and same type
// !== // not equal to and same type
// todos os operadores vao retornar verdadeiro ou falso.
// Exemplo:
console.log(1 > 2); // false
console.log(1 >= 1); // true
console.log(1 == 1); // true
console.log("a" > "A"); // false = UTF-8 = > list of unicode characters
console.log("a" == "A"); // false = ASCII
